package com.example.mvp_1.bean;

import android.R.integer;

public class Girl {
	private int head;
	private String content;
	
	public Girl(int head, String content) {
		super();
		this.head = head;
		this.content = content;
	}

	public int getHead() {
		return head;
	}

	public void setHead(int head) {
		this.head = head;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	
	
}
